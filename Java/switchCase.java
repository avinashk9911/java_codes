public class switchCase {
    public static void main(String[] args){
        int day = 3;
        switch(day){
            case 1:
                System.out.println("Day 1");
                break;
            case 2:
                System.out.println("Day 2");
                break;
            case 3:
                System.out.println("Day 3");
                break;

            default:
                System.out.println("wrong day");
        }
    }
}